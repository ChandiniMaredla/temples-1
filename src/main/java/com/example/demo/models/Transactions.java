package com.example.demo.models;

public class Transactions {
	String transactionId;
	int templeId;
	int otherDonationId;
	int annadanamDonationId;
	int transactionStatus;
	String type;
	String createdAt;
	String updatedAt;
	public String getTransactionId() {
		return transactionId;
	}
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}
	public int getTempleId() {
		return templeId;
	}
	public void setTempleId(int templeId) {
		this.templeId = templeId;
	}
	public int getOtherDonationId() {
		return otherDonationId;
	}
	public void setOtherDonationId(int otherDonationId) {
		this.otherDonationId = otherDonationId;
	}
	public int getAnnadanamDonationId() {
		return annadanamDonationId;
	}
	public void setAnnadanamDonationId(int annadanamDonationId) {
		this.annadanamDonationId = annadanamDonationId;
	}
	public int getTransactionStatus() {
		return transactionStatus;
	}
	public void setTransactionStatus(int transactionStatus) {
		this.transactionStatus = transactionStatus;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}
	public String getUpdatedAt() {
		return updatedAt;
	}
	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}

	
}
